DROP TABLE IF EXISTS T_BOARD_M;

CREATE TABLE IF NOT EXISTS T_BOARD_M (
    ID SERIAL NOT NULL,
    TITLE VARCHAR(200) NOT NULL,
    CONTENTS TEXT NOT NULL,
    READ_CNT INTEGER NOT NULL DEFAULT 0 ,
    CRT_ID VARCHAR(20),
    CRT_DT TIMESTAMP,
    MDF_ID VARCHAR(20),
    MDF_DT TIMESTAMP,
    CONSTRAINT T_BOARD_M PRIMARY KEY (ID)
);
